# Default auth key (matches firmware default)
KEY = bytes([0xAB, 0xCD, 0xEF, 0x01, 0x23, 0x45, 0x67, 0x89])

CHALLENGE_SIZE = 8
RESPONSE_SIZE = 8


def compute_response(challenge: bytes, key: bytes = KEY) -> bytes:
    """Compute the 8-byte auth response for a given challenge.

    The algorithm (verified against firmware ``FK_AuthComputeResponse``):

        1. XOR challenge bytes with key bytes
        2. SBOX substitution on each byte
        3. Rotate-left-1 diffusion: ``tmp[i] ^= ROL1(tmp[i-1])``
        4. SBOX substitution + permutation: ``resp[i] = SBOX[tmp[(i+3) % 8]]``

    Args:
        challenge: 8-byte challenge received from the host (or random).
        key: 8-byte secret key. Defaults to ``KEY``.

    Returns:
        8-byte response.

    Raises:
        ValueError: If challenge or key length is not 8.
    """
    if len(challenge) != CHALLENGE_SIZE:
        raise ValueError(f"challenge must be {CHALLENGE_SIZE} bytes, got {len(challenge)}")
    if len(key) != CHALLENGE_SIZE:
        raise ValueError(f"key must be {CHALLENGE_SIZE} bytes, got {len(key)}")

    # Step 1: XOR challenge with key
    tmp = bytearray(CHALLENGE_SIZE)
    for i in range(CHALLENGE_SIZE):
        tmp[i] = challenge[i] ^ key[i]

    # Step 2: SBOX substitution
    for i in range(CHALLENGE_SIZE):
        tmp[i] = SBOX[tmp[i]]

    # Step 3: Rotate-left-1 diffusion (tmp[0] unchanged)
    for i in range(1, CHALLENGE_SIZE):
        tmp[i] ^= ((tmp[i - 1] << 1) | (tmp[i - 1] >> 7)) & 0xFF

    # Step 4: SBOX + permutation
    resp = bytearray(CHALLENGE_SIZE)
    for i in range(CHALLENGE_SIZE):
        resp[i] = SBOX[tmp[(i + 3) % CHALLENGE_SIZE]]

    return bytes(resp)
